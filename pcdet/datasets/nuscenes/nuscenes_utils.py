    utimes = np.array([m['utime'] for m in scene_pose])
    vel_data = np.array([m['vel'][0] for m in scene_pose])
    yaw_rate = np.array([m['rotation_rate'][2] for m in scene_pose])

    l2e_t = cs_lidar['translation']
